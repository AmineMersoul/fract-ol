int	ft_islong(long long nb, const char *str)
{
	long long n1;
	long long n2;

	n1 = 2147483647;
	n2 = (n1 + 1) * -1;
	if ((!(nb <= n1 && nb >= n2)) && str[0] == '-')
		return (0);
	if (!(nb <= n1 && nb >= n2))
		return (-1);
	return (1);
}
